    private void jButtonEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEliminarActionPerformed
        String codigo = txtCodigo.getText();

    if (codigo.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Por favor, ingresa el código de la serie a eliminar.");
        return;
    }

    int confirmacion = JOptionPane.showConfirmDialog(this, "¿Estás seguro de eliminar la serie?", "Confirmar", JOptionPane.YES_NO_OPTION);
    if (confirmacion == JOptionPane.YES_OPTION) {
        Controller controller = new Controller();
        boolean eliminado = controller.eliminarSeriePorCodigo(codigo);

        if (eliminado) {
            JOptionPane.showMessageDialog(this, "Serie eliminada correctamente.");
            limpiarCampos(); 
        } else {
            JOptionPane.showMessageDialog(this, "No se encontró la serie o no se pudo eliminar.");
        }
    }
    }//GEN-LAST:event_jButtonEliminarActionPerformed

