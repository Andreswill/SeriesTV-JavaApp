    private void jButtonguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonguardarActionPerformed

    try {
        // Capturamos los datos del formulario
        String codigo = txtCodigo.getText();
        String titulo = txtTitulo.getText();
        LocalDate fecha = LocalDate.parse(txtFecha.getText()); // formato esperado: yyyy-MM-dd
        int temporadas = Integer.parseInt(txtTemporadas.getText());
        String genero = cmbGenero.getSelectedItem().toString();
        String actores = txtActores.getText();
        String sinopsis = txtSinopsis.getText();

        // Creamos el objeto Serie
        Serie serie = new Serie(codigo, titulo, fecha, temporadas, genero, actores, sinopsis);

        // Llamamos al controlador
        Controller controller = new Controller();
        boolean exito = controller.guardarSerie(serie);

        if (exito) {
            javax.swing.JOptionPane.showMessageDialog(this, "Serie guardada correctamente.");
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Error al guardar la serie.");
        }

    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    }

    }//GEN-LAST:event_jButtonguardarActionPerformed

    private void jButton1CodigoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1CodigoActionPerformed
        String codigo = txtCodigo.getText();

    Controller controller = new Controller();
    Serie serie = controller.buscarSeriePorCodigo(codigo);

    if (serie != null) {
        txtTitulo.setText(serie.getTitulo());
        txtFecha.setText(serie.getFechaLanzamiento().toString());
        txtTemporadas.setText(String.valueOf(serie.getTemporadas()));
        cmbGenero.setSelectedItem(serie.getGenero());
        txtActores.setText(serie.getActoresPrincipales());
        txtSinopsis.setText(serie.getSinopsis());
    } else {
        javax.swing.JOptionPane.showMessageDialog(this, "Serie no encontrada.");
    }
    }//GEN-LAST:event_jButton1CodigoActionPerformed

